public class AdFileVO {
	private int adfile_seq;
	private int board_seq;
	private String adfile_route;
	
	
	public int getAdfile_seq() {
		return adfile_seq;
	}
	public void setAdfile_seq(int adfile_seq) {
		this.adfile_seq = adfile_seq;
	}
	public int getBoard_seq() {
		return board_seq;
	}
	public void setBoard_seq(int board_seq) {
		this.board_seq = board_seq;
	}
	public String getAdfile_route() {
		return adfile_route;
	}
	public void setAdfile_route(String adfile_route) {
		this.adfile_route = adfile_route;
	}
	
	
}
